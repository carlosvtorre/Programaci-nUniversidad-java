    private void BCalcularMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BCalcularMouseClicked
        int opcion = CBOperacion.getSelectedIndex();
        switch (opcion) {
            case 0:
                minimo();
                break;
            case 1:
                maximo();
                break;
            case 2:
                media();
                break;
        }
    }//GEN-LAST:event_BCalcularMouseClicked
    public void imprimirDatos() {
        CTDato.setText("");
        for (int i = 0; i < num_dedos_ingresados; i++) {
            System.out.println(informacion_dactilar[i] + "\n");
        }
    }

