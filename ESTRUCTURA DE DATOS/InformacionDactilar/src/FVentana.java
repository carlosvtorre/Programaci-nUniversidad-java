    private void minimo() {
        double var_minimo = 10000;
        for (int x = 0; x < num_dedos_ingresados; x++) {
            if (informacion_dactilar[x] < var_minimo) {
                var_minimo = informacion_dactilar[x];
            }

        }
         letrero("El valor minimo es: "+var_minimo);
         
    }
    private void maximo(){
        double max = informacion_dactilar[0];
        for(int x=0; x<num_dedos_ingresados;x++){
            if(informacion_dactilar[x]>max){
                max=informacion_dactilar[x];
            }
        }
        letrero("El valor máximo es: "+max);
    }
    private void media(){
        double m=0;
        for(int x=0; x<num_dedos_ingresados;x++){
            m = m+informacion_dactilar[x];
        }
        double media;
        media = m/num_dedos_ingresados;
        letrero("El promedio es: "+media);
        
    }
