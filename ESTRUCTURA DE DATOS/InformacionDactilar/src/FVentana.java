    public void dibujar(Graphics g) {
        int x = 15, x1 = 15;
        int y = 200, y1 = 200;
        boolean valor = false;
        for (int i = 0; i < num_dedos_ingresados; i++) {
                    g.setColor(new Color (153,153,153));
                    g.fillRect(x,y-(int)informacion_dactilar[i],10,(int)informacion_dactilar[i]);
                    if (valor == true){
                        g.setColor(Color.yellow);
                        g.drawLine(x1+10, y1-(int)informacion_dactilar[i-1], x, y-(int)informacion_dactilar[i]);
                        x1+=25;
                   }
                    x+= 25;
                    valor =true;
        }
        int promedio = 0;
        int suma = 0;
        for (int i = 0; i < informacion_dactilar.length; i++) {
            suma = suma + (int) informacion_dactilar[i];
            promedio = suma / (int) informacion_dactilar.length;
        }
        g.drawString("El promedio es: " + promedio, 20, 20);
        
        double var_max = 1;
        for (int i = 0; i < informacion_dactilar.length; i++) {
            if (informacion_dactilar[i] > var_max) {
                var_max = informacion_dactilar[i];
            }
        }
        g.drawString("El valor máximo es: " + var_max, 20, 30);
        
        double var_min = 100;
        for (int i = 0; i < informacion_dactilar.length; i++) {
            if (informacion_dactilar[i] < var_min) {
                var_min = informacion_dactilar[i];
            }
        }
        g.drawString("El valor minimo es: " + var_min, 20, 40);
        
        
    }

